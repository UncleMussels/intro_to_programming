# Question: can hash values be arrays? Can you have an array of hashes? (give examples)

# Answer: hash values can be arrays, and you can have an array of hashes.

# arrays for hash values
h = {animals: ['turtle', 'somali wild ass']}
puts h

# array of hashes
animal = [{type: 'somali wild ass'}, {location: 'Somalia'}]
puts animal